def analyze_scores(scores):
    list_above_average=[]
    sum_l=0
    for x in scores:
        sum_l+=x
    average_l=sum_l/len(scores)
    for x in scores:
        if x>average_l:
            list_above_average.append(x)

    max_common=None
    max_count=0
    for x in scores:
        count=scores.count(x)
        if count>max_count:
            max_count=count
            max_common=x
    
    set_50=set()
    for x in scores:
        if x>50:
            set_50.add(x)
    
    tup_1=list()
    for x in scores:
        if x%2==0 and x%3==0:
            tup_1.append(x)
    tuple_1=tuple(tup_1)

    list_40=[]
    for x in scores:
        if x>0:
            if x<40 or x%7==0:
                list_40.append(x)
    return{
        "list_above_average": list_above_average,
        "max_common":max_common,
        "set_50":set_50,
        "tuple_1":tuple_1,
        "list_40":list_40
    }
