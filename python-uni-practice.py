class Book:
    def __init__(self,title,author,year,isbn):
        self.title=title
        self.author=author
        self.year=year
        self.isbn=isbn
    def print_info(self):
        print(f"{self.title},{self.author},{self.year},{self.isbn}")

class Reader:
    def __init__(self,first_name,last_name, reader_id):
        self.first_name=first_name
        self.last_name=last_name
        self.reader_id=reader_id
        self.borrowed_books={}
    def borrow_book(self,book,days):
        self.borrowed_books[book.isbn]=days
    def return_book(self,isbn):
        if isbn in self.borrowed_books:
            del self.borrowed_books[isbn]
    def print_info(self):
        print(f"{self.first_name},{self.last_name},{self.reader_id},{self.borrowed_books}")

class Librarian(Reader):
    def __init__(self,first_name,last_name, reader_id,position,years_of_experience):
        super().__init__(first_name, last_name, reader_id)
        self.position=position
        self.years_of_experience=years_of_experience
    def print_info(self):
        print(f"{self.first_name},{self.last_name},{self.reader_id},{self.borrowed_books},{self.position},{self.years_of_experience}")

class Library:
    def __init__(self,name):
        self.name=name
        self.books=[]
        self.readers=[]
    def add_books(self,book):
        self.books.append(book)
    def add_reader(self,reader):
        self.readers.append(reader)
    def find_book_by_isbn(self,isbn):
        for book in self.books:
            if book.isbn==isbn:
                return book
        return None
    def print_all_books(self):
        for book in self.books:
            print(f"{book.title},{book.author},{book.year},{book.isbn}")
    def print_all_readers(self):
        for reader in self.readers:
            print(f"{reader.first_name},{reader.last_name},{reader.reader_id},{reader.position},{reader.years_of_experience}")
