def remove_duplicate(lst):
    seen=set()
    unique_list=[]
    duplicates=set()
    for x in lst:
        if x in seen:
            duplicates.add(x)
        else:
            seen.add(x)
            unique_list.append(x)
    return unique_list
print(remove_duplicate([1,2,2,3,3,4,4,5,6,6,7,7,8,8]))

