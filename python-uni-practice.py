def is_strong_password(password):
    if len(password)<8:
        return False
    has_upper=False
    has_lower=False
    has_digit=False
    has_special=False
    special_chars="!@#$%^&*"
    for x in password:
        if x.isupper():
            has_upper=True
        elif x.islower():
            has_lower=True
        elif x.isdigit():
            has_digit=True
        elif x in special_chars:
            has_special=True
    return has_upper and has_lower and has_digit and has_special
